#include <stdlib.h>

char	*ft_itoa(int n)
{
	int		i;
	int		j;
	char	*a;

	i = n;
	j = 1;
	while (i /= 10)
		j++;
	if (n < 0)
		j++;
	if (!(a = (char *)malloc(j + 1)))
		return (NULL);
	if (n < 0)
	{
		a[0] = 45;
		n = -n;
	}
	a[j] = '\0';
	while (n)
	{
		a[--j] = 48 + (n % 10);
		n /= 10;
	}
	return (a);
}
