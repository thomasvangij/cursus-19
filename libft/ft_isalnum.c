#include "libft.h"

int	ft_isalnum(int c)
{
	unsigned char	d;

	d = (unsigned char)c;
	if ((d > 64 && d < 91) || (d > 96 && d < 123) || (d > 47 && d < 58))
		return (1);
	return (0);
}
