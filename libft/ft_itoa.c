#include <stdlib.h>

char	*ft_itoa(int n)
{
	int		i;
	int		j;
	char	*a;

	i = n;
	j = 1;
	if (i < 0)
		j++;
	while (i /= 10)
		j++;
	if (!(a = (char *)malloc(j + 1)))
		return (NULL);
	a[j] = '\0';
	if (n == 0)
		a[0] = '0';
	while (n)
	{
		a[0] = '-';
		if (n < 0)
			a[--j] = '0' - (n % 10);
		else
			a[--j] = '0' + (n % 10);
		n /= 10;
	}
	return (a);
}
